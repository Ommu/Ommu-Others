	public function actionIndex() 
	{
		$this->redirect(array('manage'));
	}
	
	/**
	 * Updates a particular model.
	 * If update is successful, the browser will be redirected to the 'view' page.
	 * @param integer $id the ID of the model to be updated
	 */
	public function actionSuggest($limit=15) 
	{
		if(isset($_GET['term'])) {
			$criteria = new CDbCriteria;
			$criteria->condition = 'subject LIKE :subject';
			$criteria->select	= "subject_id, subject";
			$criteria->limit = $limit;
			$criteria->order = "subject_id ASC";
			$criteria->params = array(':subject' => '%' . strtolower($_GET['term']) . '%');
			$model = BookMasterSubjects::model()->findAll($criteria);

			if($model) {
				foreach($model as $items) {
					$result[] = array('id' => $items->subject_id, 'value' => ucwords($items->subject));
				}
			} else {
				$result[] = array('id' => 0, 'value' => ucwords($_GET['term']));
			}
		}
		echo CJSON::encode($result);
		Yii::app()->end();
	}

	/**
	 * Manages all models.
	 */
	public function actionManage() 
	{
		$model=new BookMasterSubjects('search');
		$model->unsetAttributes();  // clear any default values
		if(isset($_GET['BookMasterSubjects'])) {
			$model->attributes=$_GET['BookMasterSubjects'];
		}

		$columnTemp = array();
		if(isset($_GET['GridColumn'])) {
			foreach($_GET['GridColumn'] as $key => $val) {
				if($_GET['GridColumn'][$key] == 1) {
					$columnTemp[] = $key;
				}
			}
		}
		$columns = $model->getGridColumn($columnTemp);

		$this->pageTitle = 'Book Master Subjects Manage';
		$this->pageDescription = '';
		$this->pageMeta = '';
		$this->render('/o/master_subject/admin_manage',array(
			'model'=>$model,
			'columns' => $columns,
		));
	}	
	
	/**
	 * Creates a new model.
	 * If creation is successful, the browser will be redirected to the 'view' page.
	 */
	public function actionAdd() 
	{
		$model=new BookMasterSubjects;

		// Uncomment the following line if AJAX validation is needed
		$this->performAjaxValidation($model);

		if(isset($_POST['BookMasterSubjects'])) {
			$model->attributes=$_POST['BookMasterSubjects'];
			
			$jsonError = CActiveForm::validate($model);
			if(strlen($jsonError) > 2) {
				echo $jsonError;

			} else {
				if(isset($_GET['enablesave']) && $_GET['enablesave'] == 1) {
					if($model->save()) {
						echo CJSON::encode(array(
							'type' => 5,
							'get' => Yii::app()->controller->createUrl('manage'),
							'id' => 'partial-book-master-subjects',
							'msg' => '<div class="errorSummary success"><strong>BookMasterSubjects success created.</strong></div>',
						));
					} else {
						print_r($model->getErrors());
					}
				}
			}
			Yii::app()->end();
			
		} else {
			$this->dialogDetail = true;
			$this->dialogGroundUrl = Yii::app()->controller->createUrl('manage');
			$this->dialogWidth = 600;

			$this->pageTitle = 'Create Book Master Subjects';
			$this->pageDescription = '';
			$this->pageMeta = '';
			$this->render('/o/master_subject/admin_add',array(
				'model'=>$model,
			));
		}
	}

